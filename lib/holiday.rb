def all_supplies_in_holidays(holiday_hash)
  # iterate through holiday_hash and print items such that your readout resembles:
  # Winter:
  #   Christmas: Lights, Wreath
  #   New Years: Party Hats
  # Summer:
  #   Fourth Of July: Fireworks, BBQ
  # etc.
  holiday_hash.each do |season, holidays|
    season_string = key_to_string(season)

    puts "#{season_string}:"

    holidays.each do |holiday, supplies|
      puts "  #{key_to_string(holiday)}: #{supplies.join(", ")}"
    end
  end
end

def all_holidays_with_bbq(holiday_hash)
  # return an array of holiday names (as symbols) where supply lists
  # include the string "BBQ"
  bbq_holiday_keys = []

  holiday_hash.each do |season, holidays|
    holidays.each do |holiday, supplies|
      if supplies.includes?("BBQ")
        bbq_holiday_keys << holiday
      end
    end
  end
  return bbq_holiday_keys
end

def key_to_string(key)
  array = []
  final_string = ""
  string = key.to_s
  array = string.split("_")
  array.each {|word| word.capitalize!}
  final_string = array.join(" ")
  return final_string
end

